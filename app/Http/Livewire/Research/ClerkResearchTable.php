final class ClerkResearchTable extends PowerGridComponent
{
    use ActionButton;
    use WithExport;

    public string $collegeId;

    /*
    |--------------------------------------------------------------------------
    |  Features Setup
    |--------------------------------------------------------------------------
    | Setup Table's general features
    |
    */
    public function setUp(): array
    {
        $this->showCheckBox();

        return [
            Exportable::make('export')
                ->striped()
                ->type(Exportable::TYPE_XLS, Exportable::TYPE_CSV),
            Header::make()->showSearchInput(),
            Footer::make()
                ->showPerPage()
                ->showRecordCount(),
        ];
    }

    /*
    |--------------------------------------------------------------------------
    |  Datasource
    |--------------------------------------------------------------------------
    | Provides data to your Table using a Model or Collection
    |
    */

    /**
     * PowerGrid datasource.
     *
     * @return Builder<\App\Models\Research>
     */
    public function datasource(): Builder
    {
        return Research::query()
            ->with(['authors', 'adviser', 'confirmedBy', 'facultyInCharge'])
            ->whereHas('authors.program.college', function ($query) {
                $query->where('id', $this->collegeId);
            })
            ->select('research.*');
    }

    /*
    |--------------------------------------------------------------------------
    |  Relationship Search
    |--------------------------------------------------------------------------
